    private void btAcaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAcaoActionPerformed
        //  Gerando valores aleatórios para cada componente
        // Processador
        Integer n1 = ThreadLocalRandom.current().nextInt(10, 100);
        //Memoria
        Integer n2 = ThreadLocalRandom.current().nextInt(35, 100);
        //Disco
        Integer n3 = ThreadLocalRandom.current().nextInt(1, 100);
   
        //  O cálculo da média é no seguinte raciocínio:
        //  A váriavel t(Componente) vai somando os valores aleatórios que foram gerados
        //  A váriavel c(Componente) vai contando quantas vezes o botão foi clicado
        //  Sendo assim, o cálculo da média é simplesmente dividir o total atribuido pela quantidade de cliques (quantidade de números atribuidos)
        //  Ou seja:
        //  Soma dos valores aleatórios / quantidade de vezes que os valores foram atribuidos ao vetor = média
        
        //  Média Processador
        tProcessador += n1;
        cProcessador += 1;
        float mProcessador = tProcessador/cProcessador;
        lbMedia1.setText(String.format("Média: %.0f %%", mProcessador));
        
        //  Média Memória
        tMemoria += n2;
        cMemoria += 1;
        float mMemoria = tMemoria/cMemoria;
        lbMedia2.setText(String.format("Média: %.0f %%", mMemoria));
        
        //  Média Disco
        tDisco += n3;
        cDisco += 1;
        float mDisco = tDisco/cDisco;
        lbMedia3.setText(String.format("Média: %.0f %%", mDisco));
        
        //  Alterando o valor da barra(ProgressBar) para o valor aleatório
        // Exibindo o valor atual da barra com setStringPainted
        pbPorcentagem1.setStringPainted(true);
        pbPorcentagem1.setForeground(Color.BLACK);
        pbPorcentagem1.setValue(n1);
        
        pbPorcentagem2.setStringPainted(true);
        pbPorcentagem2.setForeground(Color.BLACK);
        pbPorcentagem2.setValue(n2); 
        
        pbPorcentagem3.setStringPainted(true);
        pbPorcentagem3.setForeground(Color.BLACK);
        pbPorcentagem3.setValue(n3); 
        
        //  Valor máximo de cada componente
        //Processador
        if (maxProcessador < n1) {
            maxProcessador = n1;
        }
        lbMax1.setText(String.format("Max: %d %%", maxProcessador));  
        
        //Memoria
        if (maxMemoria < n2) {
            maxMemoria= n2;
        }
        lbMax2.setText(String.format("Max: %d %%", maxMemoria));  
        
        //Disco
        if (maxDisco < n3) {
            maxDisco = n3;
        }
        lbMax3.setText(String.format("Max: %d %%", maxDisco));  
        
        
        //  Valor mínimo de cada componente
        //Processador
        if (minProcessador > n1) {
            minProcessador = n1;
        }
        lbMin1.setText(String.format("Min: %d %%", minProcessador));
        
        //Memoria
        if (minMemoria > n2) {
            minMemoria= n2;
        }
        lbMin2.setText(String.format("Min: %d %%", minMemoria));

        //Disco
        if (minDisco > n3) {
            minDisco = n3;
        }
        lbMin3.setText(String.format("Min: %d %%", minDisco));


        //  If's para descrever o estado do componente de acordo com o tamanho do valor aleatório gerado
        // Cada estado possui uma cor para melhor análise
        if (n1 <= 60) {
            lbFrase1.setText(String.format("Normal", n1));
            lbFrase1.setForeground(Color.decode("#006400"));
        }else if (n1 >60 && n1<=80) {
            lbFrase1.setForeground(Color.decode("#cc8400"));
            lbFrase1.setText(String.format("Preocupante", n1));
        } else {
             lbFrase1.setForeground(Color.RED);
             lbFrase1.setText(String.format("Crítico", n1));
        }
        
        if (n2 <= 60) {
            lbFrase2.setText(String.format("Normal", n2));
            lbFrase2.setForeground(Color.decode("#006400"));
        }else if (n2 >60 && n2<=80) {
            lbFrase2.setText(String.format("Preocupante", n2));
            lbFrase2.setForeground(Color.decode("#cc8400"));
        } else {
            lbFrase2.setText(String.format("Crítico", n2));
             lbFrase2.setForeground(Color.RED);
        }
        
      if (n3 <= 60) {
           lbFrase3.setText(String.format("Normal", n3));
          lbFrase3.setForeground(Color.decode("#006400"));
        }else if (n3 >60 && n3<=80) {
        lbFrase3.setText(String.format("Preocupante", n3));
          lbFrase3.setForeground(Color.decode("#cc8400"));
       } else {
           lbFrase3.setText(String.format("Crítico", n3));
           lbFrase3.setForeground(Color.RED);
       }
    }//GEN-LAST:event_btAcaoActionPerformed

